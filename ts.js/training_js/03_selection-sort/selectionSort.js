/**
 * ============================================================
 *  СОРТИРОВКА ВЫБОРОМ (Selection Sort)
 * ============================================================
 *
 * ИДЕЯ:
 *   Представь, что ты раскладываешь книги по размеру.
 *   Ты смотришь на ВСЕ оставшиеся книги, находишь самую тонкую
 *   и ставишь её первой. Потом снова смотришь на все оставшиеся,
 *   находишь самую тонкую из них и ставишь второй. И так далее.
 *
 *   Алгоритм работает именно так:
 *   - Делим массив на две части: [отсортированная | неотсортированная]
 *   - На каждом шаге находим минимум в неотсортированной части
 *   - Ставим его в конец отсортированной части (один обмен!)
 *
 * КАК РАБОТАЕТ:
 *   1. Ищем минимальный элемент в ВСЁМ массиве.
 *   2. Меняем его с первым элементом.
 *   3. Ищем минимум в массиве [1..n] (без первого).
 *   4. Меняем его со вторым элементом.
 *   5. Повторяем до конца.
 *
 * ПРИМЕР (шаг за шагом):
 *   Входной массив: [5, 3, 8, 4, 2]
 *
 *   Шаг 1: ищем min в [5,3,8,4,2] → min=2 (индекс 4)
 *     Меняем arr[0] и arr[4]: [2, 3, 8, 4, 5]
 *     ✅ 2 на месте
 *
 *   Шаг 2: ищем min в [3,8,4,5] → min=3 (индекс 1)
 *     arr[1] уже на месте, обмен не нужен: [2, 3, 8, 4, 5]
 *     ✅ 3 на месте
 *
 *   Шаг 3: ищем min в [8,4,5] → min=4 (индекс 3)
 *     Меняем arr[2] и arr[3]: [2, 3, 4, 8, 5]
 *     ✅ 4 на месте
 *
 *   Шаг 4: ищем min в [8,5] → min=5 (индекс 4)
 *     Меняем arr[3] и arr[4]: [2, 3, 4, 5, 8]
 *     ✅ Готово
 *
 *   Результат: [2, 3, 4, 5, 8]
 *
 * КЛЮЧЕВОЕ СВОЙСТВО:
 *   Selection Sort делает РОВНО n-1 обменов (или меньше).
 *   Это делает его хорошим, когда операция записи ДОРОГОСТОЯЩАЯ
 *   (например, запись во флэш-память с ограниченным ресурсом).
 *
 * СЛОЖНОСТЬ:
 *   Время:   O(n²) — всегда (даже для отсортированного массива!)
 *   Память:  O(1)  — in-place
 *   Стабильность: НЕТ (обмен может нарушить порядок одинаковых)
 * ============================================================
 */

/**
 * Сортировка выбором
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив
 */
function selectionSort(arr) {
  const n = arr.length;

  // i — граница между отсортированной и неотсортированной частью
  for (let i = 0; i < n - 1; i++) {
    // Предполагаем, что минимум — первый элемент неотсортированной части
    let minIndex = i;

    // Ищем реальный минимум в неотсортированной части [i+1 .. n-1]
    for (let j = i + 1; j < n; j++) {
      if (arr[j] < arr[minIndex]) {
        minIndex = j; // нашли новый минимум
      }
    }

    // Если минимум не на своём месте — меняем
    // Важно: здесь только ОДИН обмен за итерацию!
    if (minIndex !== i) {
      [arr[i], arr[minIndex]] = [arr[minIndex], arr[i]];
    }
  }

  return arr;
}

/**
 * Двойная сортировка выбором (Double Selection Sort)
 *
 * Улучшение: за один проход находим одновременно и МИНИМУМ, и МАКСИМУМ.
 * Минимум ставим в начало, максимум — в конец.
 * Это вдвое сокращает количество итераций (но не меняет O(n²)).
 *
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив
 */
function selectionSortDouble(arr) {
  let left = 0;
  let right = arr.length - 1;

  while (left < right) {
    let minIndex = left;
    let maxIndex = right;

    // Ищем min и max в неотсортированной части [left..right]
    for (let j = left; j <= right; j++) {
      if (arr[j] < arr[minIndex]) minIndex = j;
      if (arr[j] > arr[maxIndex]) maxIndex = j;
    }

    // Ставим минимум на левую границу
    [arr[left], arr[minIndex]] = [arr[minIndex], arr[left]];

    // Внимание: если максимум был на позиции left, он теперь на minIndex!
    // Корректируем индекс максимума
    if (maxIndex === left) maxIndex = minIndex;

    // Ставим максимум на правую границу
    [arr[right], arr[maxIndex]] = [arr[maxIndex], arr[right]];

    left++;
    right--;
  }

  return arr;
}

module.exports = { selectionSort, selectionSortDouble };
