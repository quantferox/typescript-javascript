/**
 * ============================================================
 *  СОРТИРОВКА КУЧЕЙ (Heap Sort)
 * ============================================================
 *
 * ЧТО ТАКОЕ КУЧА (HEAP)?
 *   Куча — это специальное бинарное дерево со свойством:
 *   "родитель всегда больше (или равен) своих детей"
 *   Это называется Max-Heap (максимальная куча).
 *
 *   Самое важное свойство кучи:
 *   КОРЕНЬ (первый элемент) всегда содержит МАКСИМАЛЬНЫЙ элемент.
 *
 *   Хранится куча в ОБЫЧНОМ МАССИВЕ:
 *   Для элемента с индексом i:
 *     Левый ребёнок  → индекс 2*i + 1
 *     Правый ребёнок → индекс 2*i + 2
 *     Родитель       → индекс Math.floor((i-1) / 2)
 *
 *   Пример: массив [10, 7, 6, 5, 3, 2, 1] → дерево:
 *
 *           10         ← индекс 0 (корень, максимум)
 *          /   \
 *         7     6      ← индексы 1, 2
 *        / \   / \
 *       5   3 2   1    ← индексы 3, 4, 5, 6
 *
 *   Проверяем: 10 > 7 ✅, 10 > 6 ✅, 7 > 5 ✅, 7 > 3 ✅ — всё верно!
 *
 * КАК РАБОТАЕТ HEAP SORT:
 *   1. Превращаем массив в Max-Heap (build heap).
 *   2. Максимум всегда в корне (arr[0]).
 *   3. Меняем корень с последним элементом — максимум встал на своё место!
 *   4. "Убираем" последний элемент из кучи (уменьшаем размер).
 *   5. Восстанавливаем свойство кучи для нового корня (heapify).
 *   6. Повторяем шаги 3-5.
 *
 * ПРИМЕР (шаг за шагом):
 *   Начало:    [4, 10, 3, 5, 1]
 *   После build heap: [10, 5, 3, 4, 1]  → 10 в корне
 *
 *   Шаг 1: swap(0, 4) → [1, 5, 3, 4, |10|], heapify → [5, 4, 3, 1, |10|]
 *   Шаг 2: swap(0, 3) → [1, 4, 3, |5, 10|], heapify → [4, 1, 3, |5, 10|]
 *   Шаг 3: swap(0, 2) → [3, 1, |4, 5, 10|], heapify → [3, 1, |4, 5, 10|]
 *   Шаг 4: swap(0, 1) → [1, |3, 4, 5, 10|], heapify → [1, |3, 4, 5, 10|]
 *   Итог:  [1, 3, 4, 5, 10] ✅
 *
 * СЛОЖНОСТЬ:
 *   Время:   O(n log n) — всегда!
 *   Память:  O(1)       — in-place, не нужна доп. память!
 *   Стабильность: НЕТ
 *
 * Heap Sort = лучшее из двух миров:
 *   - Как Merge Sort: гарантированный O(n log n)
 *   - Как Quicksort: O(1) памяти (in-place)
 *   Но на практике медленнее Quicksort из-за плохой cache-локальности.
 * ============================================================
 */

/**
 * Heapify (просеивание вниз)
 *
 * Восстанавливает свойство Max-Heap для поддерева с корнем в индексе i.
 * Предполагается, что оба дочерних поддерева уже являются кучами.
 *
 * Алгоритм:
 *   1. Находим наибольший из: текущего узла, его левого и правого ребёнка.
 *   2. Если наибольший — не текущий узел, меняем их местами.
 *   3. Рекурсивно применяем heapify к поддереву, куда "упал" узел.
 *
 * @param {number[]} arr - массив (куча)
 * @param {number} n - размер кучи (может быть меньше arr.length!)
 * @param {number} i - индекс корня поддерева
 */
function heapify(arr, n, i) {
  let largest = i;          // предполагаем, что i — наибольший
  const left = 2 * i + 1;  // индекс левого ребёнка
  const right = 2 * i + 2; // индекс правого ребёнка

  // Если левый ребёнок существует и больше largest
  if (left < n && arr[left] > arr[largest]) {
    largest = left;
  }

  // Если правый ребёнок существует и больше largest
  if (right < n && arr[right] > arr[largest]) {
    largest = right;
  }

  // Если наибольший — не i, меняем и продолжаем просеивание
  if (largest !== i) {
    [arr[i], arr[largest]] = [arr[largest], arr[i]]; // обмен

    // Рекурсивно восстанавливаем свойство кучи ниже
    heapify(arr, n, largest);
  }
}

/**
 * Сортировка кучей (Heap Sort)
 *
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив (изменяет оригинал)
 */
function heapSort(arr) {
  const n = arr.length;

  // ── ФАЗА 1: Построение Max-Heap ──────────────────────────
  // Начинаем с последнего нелистового узла (Math.floor(n/2) - 1)
  // и идём к корню. Листья — уже "кучи" из 1 элемента.
  //
  // Почему с середины? Узлы n/2 .. n-1 — это листья (у них нет детей).
  // Их heapify ничего не изменит, поэтому начинаем с n/2 - 1.
  for (let i = Math.floor(n / 2) - 1; i >= 0; i--) {
    heapify(arr, n, i);
  }
  // После этого arr[0] = максимальный элемент!

  // ── ФАЗА 2: Извлечение элементов из кучи ─────────────────
  // На каждом шаге:
  // - arr[0] = максимум → переносим в конец (на "отсортированную" позицию)
  // - Восстанавливаем кучу для оставшейся части
  for (let i = n - 1; i > 0; i--) {
    // Максимум (arr[0]) → на позицию i (это его финальное место)
    [arr[0], arr[i]] = [arr[i], arr[0]];

    // Восстанавливаем кучу для arr[0..i-1] (без только что поставленного)
    heapify(arr, i, 0);
  }

  return arr;
}

/**
 * Итеративная версия heapify (без рекурсии)
 * Избегает стека рекурсии для очень больших массивов.
 */
function heapifyIterative(arr, n, i) {
  while (true) {
    let largest = i;
    const left = 2 * i + 1;
    const right = 2 * i + 2;

    if (left < n && arr[left] > arr[largest]) largest = left;
    if (right < n && arr[right] > arr[largest]) largest = right;

    if (largest === i) break; // нет нарушений, куча восстановлена

    [arr[i], arr[largest]] = [arr[largest], arr[i]];
    i = largest; // "опускаемся" в дерево
  }
}

/**
 * Сортировка кучей с итеративным heapify
 *
 * @param {number[]} arr
 * @returns {number[]}
 */
function heapSortIterative(arr) {
  const n = arr.length;

  // Строим Max-Heap
  for (let i = Math.floor(n / 2) - 1; i >= 0; i--) {
    heapifyIterative(arr, n, i);
  }

  // Извлекаем максимумы
  for (let i = n - 1; i > 0; i--) {
    [arr[0], arr[i]] = [arr[i], arr[0]];
    heapifyIterative(arr, i, 0);
  }

  return arr;
}

/**
 * Вспомогательная функция: строит Max-Heap из массива
 * (полезно для демонстрации структуры кучи)
 *
 * @param {number[]} arr
 * @returns {number[]} массив, организованный как Max-Heap
 */
function buildMaxHeap(arr) {
  const result = [...arr];
  const n = result.length;
  for (let i = Math.floor(n / 2) - 1; i >= 0; i--) {
    heapify(result, n, i);
  }
  return result;
}

module.exports = { heapSort, heapSortIterative, buildMaxHeap };
