/**
 * ============================================================
 *  СОРТИРОВКА ПУЗЫРЬКОМ (Bubble Sort)
 * ============================================================
 *
 * ИДЕЯ:
 *   Представь аквариум. Тяжёлые предметы лежат на дне, лёгкие
 *   пузырьки всплывают наверх. Алгоритм делает то же самое:
 *   "тяжёлые" (большие) элементы "тонут" в конец массива,
 *   а "лёгкие" (маленькие) постепенно "всплывают" к началу.
 *
 * КАК РАБОТАЕТ:
 *   1. Проходим по массиву слева направо.
 *   2. Сравниваем каждую пару соседних элементов.
 *   3. Если левый > правого — меняем их местами.
 *   4. После одного прохода наибольший элемент гарантированно
 *      оказывается на последней позиции.
 *   5. Повторяем для оставшейся части (без последнего элемента).
 *   6. Оптимизация: если за весь проход не было ни одного обмена —
 *      массив уже отсортирован, можно останавливаться досрочно.
 *
 * ПРИМЕР (шаг за шагом):
 *   Входной массив: [5, 3, 8, 4, 2]
 *
 *   Проход 1:
 *     [5, 3, 8, 4, 2] → сравниваем 5 и 3, 5 > 3 → меняем → [3, 5, 8, 4, 2]
 *     [3, 5, 8, 4, 2] → сравниваем 5 и 8, 5 < 8 → оставляем
 *     [3, 5, 8, 4, 2] → сравниваем 8 и 4, 8 > 4 → меняем → [3, 5, 4, 8, 2]
 *     [3, 5, 4, 8, 2] → сравниваем 8 и 2, 8 > 2 → меняем → [3, 5, 4, 2, 8]
 *     ✅ 8 на своём месте!
 *
 *   Проход 2: [3, 5, 4, 2, | 8]
 *     → [3, 4, 2, 5, | 8]  ✅ 5 на своём месте
 *
 *   Проход 3: [3, 4, 2, | 5, 8]
 *     → [3, 2, 4, | 5, 8]  ✅ 4 на своём месте
 *
 *   Проход 4: [3, 2, | 4, 5, 8]
 *     → [2, 3, | 4, 5, 8]  ✅ готово
 *
 *   Результат: [2, 3, 4, 5, 8]
 *
 * СЛОЖНОСТЬ:
 *   Время:   O(n²) — в среднем и худшем случае
 *            O(n)  — в лучшем (уже отсортирован, с оптимизацией)
 *   Память:  O(1)  — сортируем "на месте", доп. памяти не нужно
 *   Стабильность: ДА (одинаковые элементы не меняются местами)
 * ============================================================
 */

/**
 * Базовая реализация сортировки пузырьком
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив (изменяет оригинал)
 */
function bubbleSort(arr) {
  const n = arr.length;

  // Внешний цикл: каждая итерация "всплывает" один пузырёк на нужное место
  for (let i = 0; i < n - 1; i++) {
    // После i-й итерации последние i элементов уже на своих местах
    // Поэтому внутренний цикл идёт до n - 1 - i
    for (let j = 0; j < n - 1 - i; j++) {
      // Если левый сосед больше правого — они стоят неправильно, меняем
      if (arr[j] > arr[j + 1]) {
        // Обмен через деструктуризацию (современный JavaScript)
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
      }
    }
  }

  return arr;
}

/**
 * Оптимизированная версия с ранней остановкой
 *
 * Если за весь проход не было ни одного обмена — массив уже отсортирован.
 * Это делает алгоритм O(n) для уже отсортированных данных.
 *
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив
 */
function bubbleSortOptimized(arr) {
  const n = arr.length;

  for (let i = 0; i < n - 1; i++) {
    let swapped = false; // Флаг: был ли хоть один обмен на этом проходе?

    for (let j = 0; j < n - 1 - i; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
        swapped = true; // Запоминаем, что обмен был
      }
    }

    // Если ни одного обмена не было — массив отсортирован, выходим раньше
    if (!swapped) {
      break;
    }
  }

  return arr;
}

// Экспортируем функции для использования в тестах
module.exports = { bubbleSort, bubbleSortOptimized };
