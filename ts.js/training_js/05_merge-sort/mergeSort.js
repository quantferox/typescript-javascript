/**
 * ============================================================
 *  СОРТИРОВКА СЛИЯНИЕМ (Merge Sort)
 * ============================================================
 *
 * ИДЕЯ (стратегия "разделяй и властвуй"):
 *   Merge Sort основан на простом наблюдении:
 *   объединить два УЖЕ ОТСОРТИРОВАННЫХ массива в один
 *   отсортированный — очень легко и быстро (O(n)).
 *
 *   Используем это так:
 *   1. Делим массив пополам.
 *   2. Рекурсивно сортируем каждую половину.
 *   3. Сливаем две отсортированные половины в одну.
 *
 * АНАЛОГИЯ:
 *   Ты получил две колоды отсортированных карт.
 *   Чтобы объединить их: смотришь на верхние карты обеих колод,
 *   берёшь меньшую и кладёшь в результат. Повторяешь.
 *   Это и есть "слияние" (merge).
 *
 * ПРИМЕР слияния [1,3,5] и [2,4,6]:
 *   [1,3,5]  [2,4,6]  →  сравниваем 1 и 2 → берём 1  → результат: [1]
 *   [3,5]    [2,4,6]  →  сравниваем 3 и 2 → берём 2  → результат: [1,2]
 *   [3,5]    [4,6]    →  сравниваем 3 и 4 → берём 3  → результат: [1,2,3]
 *   [5]      [4,6]    →  сравниваем 5 и 4 → берём 4  → результат: [1,2,3,4]
 *   [5]      [6]      →  сравниваем 5 и 6 → берём 5  → результат: [1,2,3,4,5]
 *   []       [6]      →  левая пуста → берём всё правое → [1,2,3,4,5,6] ✅
 *
 * ДЕРЕВО РЕКУРСИИ для [38, 27, 43, 3, 9, 82, 10]:
 *
 *         [38,27,43,3,9,82,10]          ← делим
 *        /                   \
 *    [38,27,43]           [3,9,82,10]   ← делим
 *    /        \            /       \
 * [38,27]   [43]       [3,9]    [82,10] ← делим
 *  /    \              /   \    /    \
 * [38] [27]          [3]  [9] [82] [10] ← базовый случай
 *  \   /              \  /    \   /
 * [27,38]            [3,9]  [10,82]     ← сливаем
 *       \             /         /
 *     [27,38,43]  [3,9,10,82]           ← сливаем
 *           \     /
 *      [3,9,10,27,38,43,82]             ← итог ✅
 *
 * СЛОЖНОСТЬ:
 *   Время:   O(n log n) — всегда! (в любом случае)
 *   Память:  O(n)       — нужен доп. массив для слияния
 *   Стабильность: ДА    — порядок одинаковых сохраняется
 * ============================================================
 */

// ─────────────────────────────────────────────────────────────
// ВЕРСИЯ 1: Классическая (с созданием новых массивов)
// ─────────────────────────────────────────────────────────────

/**
 * Слияние двух отсортированных массивов в один отсортированный
 * Это основная операция Merge Sort
 *
 * @param {number[]} left - левый отсортированный массив
 * @param {number[]} right - правый отсортированный массив
 * @returns {number[]} - объединённый отсортированный массив
 */
function merge(left, right) {
  const result = [];
  let i = 0; // указатель для left
  let j = 0; // указатель для right

  // Пока в обоих массивах есть элементы — сравниваем и берём меньший
  while (i < left.length && j < right.length) {
    if (left[i] <= right[j]) {
      result.push(left[i]);
      i++;
    } else {
      result.push(right[j]);
      j++;
    }
  }

  // Добавляем остатки (один из массивов ещё не опустел)
  // concat здесь работает как "взять все оставшиеся"
  return result.concat(left.slice(i)).concat(right.slice(j));
}

/**
 * Сортировка слиянием (классическая, рекурсивная)
 *
 * @param {number[]} arr - входной массив
 * @returns {number[]} - новый отсортированный массив
 */
function mergeSort(arr) {
  // Базовый случай: массив из 0 или 1 элемента уже отсортирован
  if (arr.length <= 1) return arr;

  // Находим середину
  const mid = Math.floor(arr.length / 2);

  // Рекурсивно сортируем левую и правую половины
  const left = mergeSort(arr.slice(0, mid));
  const right = mergeSort(arr.slice(mid));

  // Сливаем две отсортированные половины
  return merge(left, right);
}

// ─────────────────────────────────────────────────────────────
// ВЕРСИЯ 2: In-place (без лишних массивов)
// ─────────────────────────────────────────────────────────────

/**
 * Слияние двух частей массива "на месте"
 * arr[left..mid] и arr[mid+1..right] — оба отсортированы
 * Результат записывается обратно в arr[left..right]
 */
function mergeInPlace(arr, left, mid, right) {
  // Копируем обе части во временный массив
  const leftPart = arr.slice(left, mid + 1);
  const rightPart = arr.slice(mid + 1, right + 1);

  let i = 0;             // указатель для leftPart
  let j = 0;             // указатель для rightPart
  let k = left;          // текущая позиция в arr

  while (i < leftPart.length && j < rightPart.length) {
    if (leftPart[i] <= rightPart[j]) {
      arr[k] = leftPart[i];
      i++;
    } else {
      arr[k] = rightPart[j];
      j++;
    }
    k++;
  }

  // Копируем остатки
  while (i < leftPart.length) { arr[k] = leftPart[i]; i++; k++; }
  while (j < rightPart.length) { arr[k] = rightPart[j]; j++; k++; }
}

/**
 * Сортировка слиянием (in-place версия)
 *
 * @param {number[]} arr - входной массив (изменяется)
 * @param {number} left - левая граница
 * @param {number} right - правая граница
 * @returns {number[]}
 */
function mergeSortInPlace(arr, left = 0, right = arr.length - 1) {
  if (left >= right) return arr;

  const mid = Math.floor((left + right) / 2);

  mergeSortInPlace(arr, left, mid);        // сортируем левую половину
  mergeSortInPlace(arr, mid + 1, right);   // сортируем правую половину
  mergeInPlace(arr, left, mid, right);     // сливаем

  return arr;
}

// ─────────────────────────────────────────────────────────────
// ВЕРСИЯ 3: Итеративная (Bottom-Up) — без рекурсии
// ─────────────────────────────────────────────────────────────

/**
 * Bottom-Up Merge Sort — итеративная версия
 *
 * Вместо рекурсии сверху вниз, работает снизу вверх:
 * 1. Сначала сливаем пары из 1 элемента → получаем пары из 2
 * 2. Затем сливаем пары из 2 → получаем группы из 4
 * 3. Удваиваем размер каждый раз, пока не сольём весь массив
 *
 * Преимущество: нет стека рекурсии → O(log n) памяти → O(1) стека
 *
 * @param {number[]} arr
 * @returns {number[]}
 */
function mergeSortBottomUp(arr) {
  const n = arr.length;

  // size — текущий размер подмассивов для слияния
  // Начинаем с 1, каждый раз удваиваем
  for (let size = 1; size < n; size *= 2) {
    // Проходим по всему массиву, сливая пары подмассивов размером size
    for (let left = 0; left < n - size; left += 2 * size) {
      const mid = left + size - 1;
      const right = Math.min(left + 2 * size - 1, n - 1);
      mergeInPlace(arr, left, mid, right);
    }
  }

  return arr;
}

module.exports = { mergeSort, mergeSortInPlace, mergeSortBottomUp };
