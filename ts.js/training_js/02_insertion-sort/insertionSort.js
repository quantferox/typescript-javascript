/**
 * ============================================================
 *  СОРТИРОВКА ВСТАВКАМИ (Insertion Sort)
 * ============================================================
 *
 * ИДЕЯ:
 *   Вспомни, как ты раскладываешь карты в пасьянсе или
 *   держишь карты в руке. Берёшь новую карту и вставляешь её
 *   на нужное место среди уже отсортированных.
 *
 *   Именно так и работает Insertion Sort:
 *   - Слева накапливается "уже отсортированная" часть
 *   - Каждый новый элемент берётся и вставляется на
 *     правильное место в уже отсортированную часть
 *
 * КАК РАБОТАЕТ:
 *   1. Считаем, что первый элемент уже "отсортирован" (он один).
 *   2. Берём второй элемент — это наш "ключ" для вставки.
 *   3. Сравниваем ключ с элементами слева от него.
 *   4. Пока элемент слева больше ключа — сдвигаем его вправо.
 *   5. Вставляем ключ на освободившееся место.
 *   6. Переходим к следующему элементу и повторяем.
 *
 * ПРИМЕР (шаг за шагом):
 *   Входной массив: [5, 3, 8, 4, 2]
 *
 *   Шаг 1: key = 3, смотрим влево
 *     [5, 3, 8, 4, 2] → 5 > 3, сдвигаем 5 вправо → [_, 5, 8, 4, 2]
 *     Слева больше нет → вставляем 3 → [3, 5, 8, 4, 2]
 *
 *   Шаг 2: key = 8, смотрим влево
 *     [3, 5, 8, 4, 2] → 5 < 8, стоп → вставляем 8 на месте
 *     [3, 5, 8, 4, 2] — не изменился
 *
 *   Шаг 3: key = 4, смотрим влево
 *     8 > 4 → сдвигаем 8 → [3, 5, _, 8, 2]
 *     5 > 4 → сдвигаем 5 → [3, _, 5, 8, 2]
 *     3 < 4 → стоп → вставляем 4 → [3, 4, 5, 8, 2]
 *
 *   Шаг 4: key = 2, смотрим влево
 *     8 > 2, 5 > 2, 4 > 2, 3 > 2 → сдвигаем всё
 *     → [_, 3, 4, 5, 8] → вставляем 2 → [2, 3, 4, 5, 8]
 *
 *   Результат: [2, 3, 4, 5, 8] ✅
 *
 * СЛОЖНОСТЬ:
 *   Время:   O(n²) — средний и худший случай
 *            O(n)  — лучший (массив уже отсортирован)
 *   Память:  O(1)  — in-place
 *   Стабильность: ДА
 * ============================================================
 */

/**
 * Сортировка вставками
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив (изменяет оригинал)
 */
function insertionSort(arr) {
  const n = arr.length;

  // Начинаем с 1: первый элемент считается уже "отсортированным"
  for (let i = 1; i < n; i++) {
    // Запоминаем текущий элемент — это наш "ключ для вставки"
    const key = arr[i];

    // j — позиция последнего элемента в отсортированной части
    let j = i - 1;

    // Сдвигаем элементы вправо, пока они больше ключа
    // Это освобождает место для вставки ключа
    while (j >= 0 && arr[j] > key) {
      arr[j + 1] = arr[j]; // сдвигаем элемент на позицию правее
      j--;                  // идём левее
    }

    // j + 1 — первая позиция, где элемент не больше ключа
    // Вставляем ключ
    arr[j + 1] = key;
  }

  return arr;
}

/**
 * Сортировка вставками с бинарным поиском
 *
 * Улучшение: вместо линейного поиска места для вставки
 * используем бинарный поиск — это снижает количество СРАВНЕНИЙ
 * до O(n log n), но количество СДВИГОВ всё равно O(n²).
 * На практике работает быстрее для больших массивов.
 *
 * @param {number[]} arr - входной массив
 * @returns {number[]} - отсортированный массив
 */
function insertionSortBinary(arr) {
  const n = arr.length;

  for (let i = 1; i < n; i++) {
    const key = arr[i];

    // Бинарный поиск позиции для вставки в диапазоне [0, i)
    let left = 0;
    let right = i;

    while (left < right) {
      const mid = Math.floor((left + right) / 2);
      if (arr[mid] <= key) {
        left = mid + 1; // ищем правее
      } else {
        right = mid;    // ищем левее
      }
    }

    // left — позиция для вставки. Сдвигаем элементы правее left
    for (let j = i; j > left; j--) {
      arr[j] = arr[j - 1];
    }

    arr[left] = key; // вставляем
  }

  return arr;
}

module.exports = { insertionSort, insertionSortBinary };
